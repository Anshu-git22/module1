#include <stdio.h>

int main() {
    // Variable declaration
    int age = 20;
    float marks = 85.5;
    char grade = 'A';

    // Constant declaration
    const float PI = 3.14;

    // Display values
    printf("Age: %d\n", age);
    printf("Marks: %.2f\n", marks);
    printf("Grade: %c\n", grade);
    printf("PI: %.2f\n", PI);

    return 0;
}
