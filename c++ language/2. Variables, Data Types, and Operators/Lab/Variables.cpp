#include <iostream>
using namespace std;

int main() {
    int a = 10;
    float b = 5.5;
    char ch = 'A';
    const float PI = 3.14;

    cout << "Integer: " << a << endl;
    cout << "Float: " << b << endl;
    cout << "Character: " << ch << endl;
    cout << "Constant PI: " << PI << endl;

    cout << "Addition: " << a + b << endl;

    return 0;
}
